# Запрашиваем у пользователя длину стороны 'a'
# Функция input() получает данные как строку, поэтому используем float() для преобразования в число
try:
    a = float(input("Введите длину стороны 'a': "))
    
    # Вычисляем периметр по формуле P = 4 * a
    P = 4 * a
    
    # Выводим результат
    print(f"Периметр (P) равен: {P}")

except ValueError:
    print("Ошибка: Введено некорректное значение. Пожалуйста, введите число.")