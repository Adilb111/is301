try:
    a = float(input("Введите длину стороны a: "))
    b = float(input("Введите длину стороны b: "))

    # 1. Вычисляем площадь (S = a * b)
    S = a * b
    
    # 2. Вычисляем периметр (P = 2 * (a + b))
    P = 2 * (a + b)
    
    # Выводим результаты
    print("\n--- Результаты ---")
    print(f"Сторона a = {a}")
    print(f"Сторона b = {b}")
    print(f"Площадь (S) = {S}")
    print(f"Периметр (P) = {P}")

except ValueError:
    print("Ошибка: Введено некорректное значение. Пожалуйста, введите числа для сторон.")