# Запрашиваем у пользователя длину стороны 'a'
# Преобразуем введенное значение в число с плавающей точкой (float)
try:
    a = float(input("Введите длину стороны квадрата (a): "))
    
    # Вычисляем площадь по формуле S = a**2 (оператор ** - это возведение в степень)
    S = a ** 2
    
    # Выводим результат
    print(f"Площадь квадрата (S) равна: {S}")

except ValueError:
    print("Ошибка: Введено некорректное значение. Пожалуйста, введите число.")